values = {
    "A": 0,
    "B": 1,
    "C": 2,

    "X": 0,
    "Y": 1,
    "Z": 2
}


def solve_part_1(rounds):
    score = 0
    for round in rounds:
        val1 = values[round[0]]
        val2 = values[round[1]]
        score += val2 + 1
        if val2 == (val1 + 1) % 3:
            # you win
            score += 6
        elif val1 == (val2 + 1) % 3:
            # you lose
            continue
        else:
            # draw
            score += 3
    return score


def solve_part_2(rounds):
    score = 0
    for round in rounds:
        val1 = values[round[0]]
        val2 = values[round[1]]
        if val2 == 0:
            # lose
            score += (val1 + 2) % 3 + 1
        if val2 == 1:
            # draw
            score += val1 + 1
            score += 3
        if val2 == 2:
            # win
            score += (val1 + 1) % 3 + 1
            score += 6
    return score


def solve(puzzle_input):
    rounds = [[y for y in x.split(" ")]
              for x in puzzle_input.strip().split("\n")]

    print(solve_part_1(rounds))
    print(solve_part_2(rounds))

    return


