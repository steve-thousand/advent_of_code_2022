def value_of_badge(badge):
    ascii = ord(badge)
    if ascii > 96:
        return ascii - 96
    else:
        return ascii - 64 + 26


def solve(puzzle_input):
    compartments = [[y for y in x] for x in puzzle_input.strip().split("\n")]

    sum = 0
    for compartment in compartments:
        length = int(len(compartment) / 2)
        left = set(compartment[:length])
        right = set(compartment[length:])
        common = next(iter(left.intersection(right)))
        sum += value_of_badge(common)

    sum2 = 0
    for i in range(0, int(len(compartments) / 3)):
        common = next(iter(set(compartments[i * 3 + 0]).intersection(
            set(compartments[i * 3 + 1])).intersection(set(compartments[i * 3 + 2]))))
        sum2 += value_of_badge(common)

    print(sum)
    print(sum2)

    return


